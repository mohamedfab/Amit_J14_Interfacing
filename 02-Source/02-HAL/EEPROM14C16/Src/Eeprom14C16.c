/******************************************************************************
* \Syntax          : void Eeprom14C32(void)
* \Description     : Describe this service
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : None
* \Return value:   : None
*******************************************************************************/
void Eeprom14C32_Init(void)
{
	I2C_MasterInit();
}
/******************************************************************************
* \Syntax          : void Eeprom14C32_WriteByte(u8 addr, u8 data)
* \Description     : Describe this service
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : addr	- address of byte
* 				   : data   - data byte to be written
* \Return value:   : None
*******************************************************************************/
void Eeprom14C32_WriteByte(u8 addr, u8 data)
{
	I2C_StartCondition();
	I2C_Send_SlaveAddressWriteOperation(0b10100000);
	I2C_WriteByte(addr);
	I2C_WriteByte(data);
	I2C_StopCondition();
}
u8 Eeprom14C32_ReadByte(u8 addr)
{
	u8 loc_dataRead=0;
	I2C_StartCondition();
	I2C_Send_SlaveAddressWriteOperation(0b10100000);
	I2C_WriteByte(addr);
	I2C_RepeatedStartCondition();
	I2C_Send_SlaveAddressReadOperation(0b10100000);
	loc_dataRead = I2C_ReadByte();
	I2C_StopCondition();
	return loc_dataRead;
}
