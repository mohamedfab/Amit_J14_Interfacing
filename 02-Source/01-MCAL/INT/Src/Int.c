/******************************************************************************
* \Syntax          : void INT_GlobalEnable(void)
* \Description     : Describe this service
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : None
* \Return value:   : None
*******************************************************************************/
void INT_GlobalEnable(void)
{
	sei();
}
/******************************************************************************
* \Syntax          : void INT_GlobalDisable(void)
* \Description     : Describe this service
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : None
* \Return value:   : None
*******************************************************************************/
void INT_GlobalDisable(void)
{
	cli();
}
/******************************************************************************
* \Syntax          : void INT_ExtEnable(Int_ExtIntID IntId)
* \Description     : Describe this service
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : IntId -  External Interrupt ID
* \Return value:   : None
*******************************************************************************/
void INT_ExtEnable(Int_ExtIntID IntId)
{
	switch (IntId)
	{
	case INT0_ID:
		SET_BIT(INT_GICR_REG,INT0_Enable_BIT_NO);
		break;
	case INT1_ID:
		SET_BIT(INT_GICR_REG,INT1_Enable_BIT_NO);
		break;
	case INT2_ID:
		SET_BIT(INT_GICR_REG,INT2_Enable_BIT_NO);
		break;
	}
}
